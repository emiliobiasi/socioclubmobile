class ColorScheme {
  constructor(
    title,
    background,
    primary,
    icons,
    div,
    text,
    secondaryText,
    button
  ) {
    this.title = title;
    this.background = background;
    this.primary = primary;
    this.icons = icons;
    this.div = div;
    this.text = text;
    this.secondaryText = secondaryText;
    this.button = button;
  }
}
